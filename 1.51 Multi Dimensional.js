// Multi dimensional array

var arr = [
    [92, 87, 93],
    [87, 77, 90],
    [83, 70, 92]
]


for(var i = 0; i < arr.length; i++){
    var singleArray = arr[i];
    for(var j = 0; j < singleArray.length; j++){
        console.log('Element ' + i + ':' + singleArray[j]);
    }
}



